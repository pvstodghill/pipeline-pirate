#! /bin/bash

. $(dirname ${BASH_SOURCE[0]})/doit-preamble.bash

# ------------------------------------------------------------------------

rm -rf ${PIRATE}

# ------------------------------------------------------------------------
# Run Pirate
# ------------------------------------------------------------------------

echo 1>&2 '# Run Pirate'

PIRATE --align \
       --input ${INPUTS} \
       --output ${PIRATE} \
       --threads ${THREADS} \
       ${PIRATE_ARGS}

# ------------------------------------------------------------------------
# Done.
# ------------------------------------------------------------------------

echo 1>&2 '# Done.'

